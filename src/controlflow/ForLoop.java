package controlflow;

public class ForLoop {


    public static void main(String[] args) {

        for(int i=0; i<10; i++){
            System.out.println("Bangladesh"+i);
        }
        int [] myNumbers={10,20,30,40,50};
        for(int i=0; i<myNumbers.length; i++){
            System.out.println(myNumbers[i]);
        }
    }
}
